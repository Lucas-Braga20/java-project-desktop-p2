    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        try {
            Float valor = Float.parseFloat(jTextFieldValor.getText());
            Integer quantidade = Integer.parseInt(jTextFieldQuantidade.getText());
            Produto produto = (Produto) jComboBoxProduto.getSelectedItem();
            
            ProdutoPedidoBuilder produtoPedidoBuilder = new ProdutoPedidoBuilder(valor, quantidade, produto, pedido);
            
            if (produtoPedido == null) {
                ProdutoPedidoUC.salvar(produtoPedidoBuilder);
            } else {
                ProdutoPedidoUC.atualizar(produtoPedidoBuilder, produtoPedido);
            }
            
            dispose();
        } catch (Exception e) {
            System.out.println(e);
        }

        dispose();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

