    public void carregarProdutoPedido() {
        jTextFieldValor.setText(produtoPedido.getValorUnitario().toString());
        jTextFieldQuantidade.setText(produtoPedido.getQuantidade().toString());
        jComboBoxProduto.setSelectedItem(produtoPedido.getProduto());
    }
    
    public void carregarProdutos() {
        try {
            List<Produto> produtos = new ProdutoUC().listarTodos();
            jComboBoxProduto.setModel(new DefaultComboBoxModel(produtos.toArray()));
        } catch (Exception e) {
            System.out.println(e);
        }
    }

